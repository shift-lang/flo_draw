use futures::executor;
use futures::prelude::*;

use flo_curves::bezier::path::*;
use flo_draw::canvas::*;
use flo_draw::*;

///
/// Draws FlowBetween's mascot onto a texture, then draw the texture onto a window (so it's possible to see the pixellation)
///
pub fn main() {
    with_2d_graphics(|| {
        // Decode
        let mascot = decode_drawing(MASCOT.chars())
            .collect::<Result<Vec<Draw>, _>>()
            .unwrap();

        // Convert the mascot to a set of paths (note we skip the setup steps here so the paths are not affected by the initial transformation matrix)
        let render_mascot = stream::iter(mascot.into_iter().skip(4));
        let mascot_paths = drawing_to_attributed_paths::<SimpleBezierPath, _>(render_mascot);
        let mascot_paths =
            executor::block_on(async move { mascot_paths.collect::<Vec<_>>().await });

        let canvas = create_drawing_window("Pixelated Flo");
        canvas.draw(|gc| {
            gc.clear_canvas(Color::Rgba(1.0, 0.9, 0.8, 1.0));
            gc.canvas_height(1000.0);
            gc.transform(Transform2D::scale(1.0, -1.0));
            gc.center_region(0.0, 0.0, 1000.0, 1000.0);

            // Draw the mascot to a sprite
            gc.sprite(SpriteId(0));
            gc.clear_sprite();
            for (attributes, path_set) in mascot_paths.iter() {
                gc.render_bezier_shape(attributes, path_set);
            }

            gc.layer(LayerId(0));

            // Draw the sprite to a texture
            let flo_w = 128;
            let flo_h = 128;

            gc.create_texture(TextureId(0), flo_w, flo_h, TextureFormat::Rgba);
            gc.set_texture_from_sprite(TextureId(0), SpriteId(0), 0.0, 0.0, 1024.0, 1024.0);

            // Draw the texture to the display
            gc.new_path();
            gc.rect(0.0, 0.0, 1000.0, 1000.0);
            gc.fill_texture(TextureId(0), 0.0, 0.0, 1000.0, 1000.0);
            //gc.fill_color(Color::Rgba(0.0, 0.0, 0.0, 1.0));
            gc.fill();
        });
    });
}

